package com.java_july_10am_prcte;

public class Nested_For_Loop_pt {

	public static void main(String[] args) {
		for (int i = 3; i >= 0; i--) {
			for (int j = 3; j >= 0; j--) {
				for (int k = 1; k >= 0; k--) {

					System.out.println(i + "" + j + "" + k);

				}

			}

			System.out.println();
		}
	}

}
